// Values are within ~1–2% of catalogue tables (difference due to root fillets).
//
// Unit convention:
//   b, t   → mm   (leg width, thickness)
//   A      → cm²  (cross-sectional area)
//   I1     → cm⁴  (moment of inertia about centroidal axis parallel to leg = (Iu + Iv)/2)
//   iv     → cm   (radius of gyration about minor principal axis v)
//   e      → cm   (centroid distance from back of leg)

